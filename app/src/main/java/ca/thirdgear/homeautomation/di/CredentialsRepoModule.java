package ca.thirdgear.homeautomation.di;

import ca.thirdgear.homeautomation.repository.CredentialsRepository;
import dagger.Module;
import dagger.Provides;

/**
 * Define the credentials repository to be included as part of the dependency chain.
 */
@Module
public class CredentialsRepoModule
{
    public CredentialsRepoModule()
    {
        //do nothing
    }

    @Provides
    CredentialsRepository provideCredentialsRepository()
    {
        return new CredentialsRepository();
    }
}
