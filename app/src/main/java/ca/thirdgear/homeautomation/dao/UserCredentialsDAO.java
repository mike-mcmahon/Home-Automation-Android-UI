package ca.thirdgear.homeautomation.dao;

import androidx.lifecycle.LiveData;
import androidx.room.Dao;
import androidx.room.Insert;
import androidx.room.OnConflictStrategy;
import androidx.room.Query;
import ca.thirdgear.homeautomation.entity.UserCredentials;

/**
 * Database - Data Access Object - for UserCredentials
 */
@Dao
public interface UserCredentialsDAO
{
    @Query("SELECT * FROM user_credentials LIMIT 1")
    LiveData<UserCredentials> getObservableUser();

    @Query("SELECT * FROM user_credentials LIMIT 1")
    UserCredentials getUser();

    @Query("SELECT COUNT(*) FROM user_credentials")
    int getNumberOfUsers();

    @Query("DELETE FROM user_credentials")
    void deleteAll();

    @Insert(onConflict = OnConflictStrategy.REPLACE)
    void insert(UserCredentials credentials);
}
