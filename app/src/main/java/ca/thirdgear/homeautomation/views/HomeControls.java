package ca.thirdgear.homeautomation.views;

import android.os.Bundle;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.Button;
import androidx.fragment.app.Fragment;
import androidx.navigation.*;
import ca.thirdgear.homeautomation.R;


/**
 * Fragment used to display the home controls menu, and provides navigation to lighting and garage door controls.
 */
public class HomeControls extends Fragment
{
    private Button navToLightingControlsButton;
    private Button navToGarageControlButton;
    private Button navToSystemStatusButton;

    public HomeControls()
    {
        //do nothing
    }

    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState)
    {
        setHasOptionsMenu(true);
        return inflater.inflate(R.layout.home_controls_fragment, container, false);
    }

    @Override
    public void onViewCreated(View view, Bundle savedInstanceState)
    {
        super.onViewCreated(view, savedInstanceState);

        //Implement NavOptions
        final NavOptions options = new NavOptions.Builder()
                .setEnterAnim(R.anim.slide_in_right)
                .setExitAnim(R.anim.slide_out_left)
                .setPopEnterAnim(R.anim.slide_in_left)
                .setPopExitAnim(R.anim.slide_out_right)
                .build();

        navToLightingControlsButton = getView().findViewById(R.id.lighting_controls_button);
        navToGarageControlButton = getView().findViewById(R.id.garage_control_button);
        navToSystemStatusButton = getView().findViewById(R.id.system_status_button);

        navToLightingControlsButton.setOnClickListener(new View.OnClickListener()
        {
            public void onClick(View view)
            {
                Navigation.findNavController(view).navigate(R.id.lighting_dest, null, options);
            }
        });

        navToGarageControlButton.setOnClickListener(new View.OnClickListener()
        {
            public void onClick(View view)
            {
                Navigation.findNavController(view).navigate(R.id.garage_dest, null, options);
            }
        });

        navToSystemStatusButton.setOnClickListener(new View.OnClickListener()
        {
            public void onClick(View view)
            {
                Navigation.findNavController(view).navigate(R.id.system_status_dest, null, options);
            }
        });

    }

}
