package ca.thirdgear.homeautomation.http;

import okhttp3.Interceptor;
import okhttp3.Request;
import okhttp3.Response;
import java.io.IOException;

/**
 * Class intercepts http requests and adds basic authorization credentials to header.
 */
public class CredentialsInterceptor implements Interceptor
{
    private String authToken;

    public CredentialsInterceptor(String token)
    {
        this.authToken = token;
    }

    /**
     * Intercept outgoing http requests and add basic authorization with user credentials.
     * @param chain
     * @return Response
     * @throws IOException
     */
    @Override
    public Response intercept(Chain chain) throws IOException
    {
        Request originalRequest = chain.request();

        Request.Builder builder = originalRequest.newBuilder()
                .header("Authorization", authToken);

        Request newRequest = builder.build();

        return chain.proceed(newRequest);
    }
}
