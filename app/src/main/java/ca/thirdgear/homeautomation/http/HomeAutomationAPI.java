package ca.thirdgear.homeautomation.http;

import ca.thirdgear.homeautomation.model.NetworkApiResponse;
import retrofit2.Call;
import retrofit2.http.GET;
import retrofit2.http.POST;

/**
 * Define the HTTP network calls to remote API.
 */
public interface HomeAutomationAPI
{
    @GET("iostatus_oneshot")
    Call<NetworkApiResponse> getIoStatus();

    @POST("zoneone")
    Call<NetworkApiResponse> toggleZoneOne();

    @POST("zonetwo")
    Call<NetworkApiResponse> toggleZoneTwo();

    @POST("garagerelay")
    Call<NetworkApiResponse> toggleGarageDoor();

}
