package ca.thirdgear.homeautomation.geofencing;

import android.content.Context;
import android.content.res.Resources;
import ca.thirdgear.homeautomation.R;
import com.google.android.gms.common.api.ApiException;
import com.google.android.gms.location.GeofenceStatusCodes;

/**
 * Geofence error codes mapped to error messages.
 */
public class GeofenceErrorMessages
{
    /**
     * Prevents instantiation.
     */
    private GeofenceErrorMessages()
    {
        //do nothing
    }

    /**
     * Returns the error string for a geofencing exception.
     * @param context
     * @param e
     * @return error string
     */
    public static String getErrorString(Context context, Exception e)
    {
        if (e instanceof ApiException)
        {
            return getErrorString(context, ((ApiException) e).getStatusCode());
        }
        else
        {
            return context.getResources().getString(R.string.unknown_geofence_error);
        }
    }

    /**
     * Returns the error string for a geofencing error code.
     * @param context
     * @param errorCode
     * @return error string
     */
    public static String getErrorString(Context context, int errorCode)
    {
        Resources resources = context.getResources();
        switch (errorCode)
        {
            case GeofenceStatusCodes.GEOFENCE_NOT_AVAILABLE:
                return resources.getString(R.string.geofence_not_available);

            case GeofenceStatusCodes.GEOFENCE_TOO_MANY_GEOFENCES:
                return resources.getString(R.string.geofence_too_many_geofences);

            case GeofenceStatusCodes.GEOFENCE_TOO_MANY_PENDING_INTENTS:
                return resources.getString(R.string.geofence_too_many_pending_intents);

            default:
                return resources.getString(R.string.unknown_geofence_error);
        }
    }
}
