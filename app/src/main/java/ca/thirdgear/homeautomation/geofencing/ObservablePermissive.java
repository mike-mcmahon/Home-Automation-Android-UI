package ca.thirdgear.homeautomation.geofencing;

import java.util.Observable;

/**
 * Used to communicate between GeofenceBroadcastReceiver and Garage.
 */
public class ObservablePermissive extends Observable
{
    private static ObservablePermissive instance = new ObservablePermissive();
    private boolean permissive;

    public static ObservablePermissive getInstance()
    {
        return instance;
    }

    private ObservablePermissive()
    {
        //do nothing
    }

    /**
     * return boolean permissive value from broadcast receiver.
     * @return boolean permissive.
     */
    public boolean getPermissive()
    {
        return permissive;
    }

    /**
     * update the value of the boolean permissive in this observable.
     * @param permissive
     */
    public void updateValue(boolean permissive)
    {
        synchronized (this)
        {
            this.permissive = permissive;
            setChanged();
            notifyObservers();
        }
    }
}
