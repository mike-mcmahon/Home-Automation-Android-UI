package ca.thirdgear.homeautomation;

import android.app.Application;
import ca.thirdgear.homeautomation.di.*;

/**
 *  Instantiate components - instances should be declared only once throughout the entire lifespan of the application.
 *  Only inject @Singleton object instances from this component, otherwise they won't be the same instance.
 */
public class HomeAutomationApp extends Application //implements LocationListener
{
    private static ApplicationComponents applicationComponents;

    @Override
    public void onCreate()
    {
        super.onCreate();

        applicationComponents = DaggerApplicationComponents.builder()
                .geofencingServiceModule(new GeofencingServiceModule(this))
                .databaseModule(new DatabaseModule(this))
                .credentialsRepoModule(new CredentialsRepoModule())
                .dataRepositoryModule(new DataRepositoryModule())
                .networkModule(new NetworkModule())
                .build();
    }

    //call this method in classes that require the dependencies built here.
    public static ApplicationComponents getAppComponent()
    {
        return applicationComponents;
    }
}
